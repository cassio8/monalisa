	<h1>Introduction to Monalisa</h1>
	<p>Monalisa is a web framework created by Vespera Tecnologia based on Kube, an already existing framework.</p>

    <h2>Components</h2>
    <p>Components are elements that you will certainly use frequently in the development of your web project. From buttons to perform certain actions to tables to format information, monalisa makes several of these components available by default. You can explore more of these elements in the components session.</p>

    <h2>Modifiers</h2>
    <p>Modifiers are very useful elements in monalisa. You can use it when you need to modify some property of an element.
    These modifiers in monalisa always come with an <strong>underline prefix</strong>, for example we have the _upper modifier that turns the text into uppercase.</p>

