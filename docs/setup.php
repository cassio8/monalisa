	<h1>Setup Monalisa</h1>
	<p>With a few steps you can integrate monalisa into your web project and start using it.</p>

    <h2>Download</h2>
    <p>First of all download monalisa. You can do it by clicking on the link below.</p>
 
    <a href="../monalisa.zip" download><button class="Button _download">Download Here</button></a>

    <h2>Set up</h2>
    <p>Now incorporate monalisa into your project by adding this tag in your header with the path to the monalisa style file.</p>

	<pre><code class="language-html">&#60;link rel="stylesheet" href="path/to/monalisa.css"&#62</code></pre>

	<p>If you choose you can use the minified version of monalisa.</p>

	<pre><code class="language-html">&#60;link rel="stylesheet" href="path/to/monalisa.min.css"&#62</code></pre>

	<p>If you want to explore more, we make available the code made in SASS of the monalisa used in its creation and then compiled for the versions in css. The code is inside the scss folder.</p>

